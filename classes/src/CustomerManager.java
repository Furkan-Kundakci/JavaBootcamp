public class CustomerManager {
    public void add(String customerName) {
        System.out.println(customerName + " Müşterisi eklendi.");
    }

    public void remove(String customerName) {
        System.out.println(customerName + " Müşterisi silindi.");
    }

    public void update(String customerName) {
        System.out.println(customerName + " Müşterisi güncellendi");
    }
}
